def merge_locale(lang: str):
    path = LOCALES / f'{lang}.json'
    with path.open(encoding='utf-8') as f:
        data = json.load(f)

    # Write under the `dash` namespace (fresh; `dashboard` namespace is legacy
    # and used by no current components).
    if 'dash' in data:
        print(f'[{lang}] replacing existing `dash` namespace')
    data['dash'] = DASH[lang]

    with path.open('w', encoding='utf-8', newline='\n') as f:
        json.dump(data, f, indent=2, ensure_ascii=False)
        f.write('\n')
    print(f'[{lang}] wrote dash namespace -> {path.relative_to(ROOT)}')


for lang in ('en', 'nl', 'fr', 'de'):
    merge_locale(lang)
